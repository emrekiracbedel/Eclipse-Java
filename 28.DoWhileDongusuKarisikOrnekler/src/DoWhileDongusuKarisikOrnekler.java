
public class DoWhileDongusuKarisikOrnekler {

	public static void main(String[] args) {

		// Break - Continue Kullanımı
		
		
		int sayiyiBul;
		
		for (int i = 1; i <=10; i++) {
			
			if (i==4) {
				
				sayiyiBul=i;
				break;
				
			}
			System.out.println(i);
		}
		
		
		
	}

}
