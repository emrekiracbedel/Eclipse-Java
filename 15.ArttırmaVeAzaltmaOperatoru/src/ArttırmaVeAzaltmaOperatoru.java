
public class ArttırmaVeAzaltmaOperatoru {

	public static void main(String[] args) {

		// Arttırma: ++ ----> 1 arttırmak için kullanılan bir operatör
		// Azaltma: -- ----> 1 azaltmak için kullanılan bir operatör

		int sayi1 = 5;

		sayi1++;

		int sayi2 = 7;

		sayi2--;

		System.out.println(sayi1);
		System.out.println(sayi2);
	}

}
