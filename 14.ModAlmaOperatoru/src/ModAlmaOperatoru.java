
public class ModAlmaOperatoru {

	public static void main(String[] args) {

		// % Operatörü (Mod Alma) Bir sayının bir sayıya bölümünden kalanı bulmak için kullanılır
		
		int sayi1=10;
		int sayi2=3;
		
		// 10/3 den kalan 1 olacağı için % bize 1 sonucunu verecektir
		
		int sonuc=sayi1%sayi2;
		
		System.out.println("Sonuç: "+sonuc);
		
	}

}
