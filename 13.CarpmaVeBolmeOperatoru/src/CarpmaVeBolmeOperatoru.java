
public class CarpmaVeBolmeOperatoru {

	public static void main(String[] args) {

		// Çarpma (*) ve Bölme (/) Operatörleri
		
		int sayi1=10;
		int sayi2=5;
		
		int sonuc=sayi1/sayi2;
				
		System.out.println("Sonuç: "+sonuc);
		
		int sayi3=6;
		int sayi4=7;
		
		int sonuc2=sayi3*sayi4;
		System.out.println("Sonuç2: "+sonuc2);
	}

}
