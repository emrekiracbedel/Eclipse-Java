
public class ContinueKullanimi {

	public static void main(String[] args) {

		// Continue Kullanımı -- Döngüyü sadece bir defa kırar
		
		
		
		for (int i = 1; i <=10; i++) {
			
			if (i==4 || i==8) {
				
				
				continue;
				
			}
			
			System.out.println(i);
			
		}
		
		
		
	}

}
