
public class BooleanVeriTipi {

	public static void main(String[] args) {

		// Doğru yada Yanlış için kullanılır

		boolean sonuc = 5 > 3;
		boolean sonuc2 = 5 < 3;

		// = eşittir demek değil, == eşittir demektir
		boolean sonuc3 = 5 == 5;

		System.out.println("Sonuç1: " + sonuc);
		System.out.println("Sonuç2: " + sonuc2);
		System.out.println("Sonuç3: " + sonuc3);
	}

}
